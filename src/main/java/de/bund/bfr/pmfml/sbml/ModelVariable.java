package de.bund.bfr.pmfml.sbml;

import java.util.Locale;

public class ModelVariable {

  String name;
  Double value;

  public ModelVariable(final String name, final Double value) {
    this.name = name;
    this.value = value;
  }

  public String getName() {
    return name;
  }

  public Double getValue() {
    return value;
  }

  @Override
  public String toString() {
    if (value == null)
      return String.format(Locale.ENGLISH, "ModelVariable [name=%s]", name);
    return String.format(Locale.ENGLISH, "ModelVariable [name=%s, value=%.6f]", name, value);
  }

  @Override
  public boolean equals(final Object obj) {
    if (this == obj)
      return true;
    if (obj == null)
      return false;
    if (getClass() != obj.getClass())
      return false;
    ModelVariable other = (ModelVariable) obj;
    if (name == null) {
      if (other.name != null)
        return false;
    } else if (!name.equals(other.name))
      return false;
    if (value == null) {
      if (other.value != null)
        return false;
    } else if (!value.equals(other.value))
      return false;
    return true;
  }
}
